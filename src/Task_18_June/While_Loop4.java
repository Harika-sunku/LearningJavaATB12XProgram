package Task_18_June;

public class While_Loop4 {
    public static void main(String[] args) {

        //sum of first 10 natural numbers

        int sum=0;
        int i=1;
        while(i<=10){
            sum+=i;
            i++;

        }
        System.out.println("sum of first 10 natural numbers is "+sum);

    }
}
