package Ex_15_Arrays;

public class Lab151_Arrays_Print {

    public static void main(String[] args) {

        int[] a={40,50,60,70};
        for(int b:a){
            System.out.println(b);
        }
        System.out.println(" ---");
        for(int i=0;i<a.length;i++){
            System.out.println(a[i]);
        }

    }
}
