package Ex_08_Switch;

public class Lab101_Interview {

    public static void main(String[] args) {


        switch (-1){
            default:
                System.out.println("Default");
                break;
            case -1:
                System.out.println("10");
                break;
            case 9:
                System.out.println("9");
                break;
        }

    }

}
